import { ref, watch, readonly } from 'vue';

export type Locale = 'en' | 'fr' | 'ar';

const currentLocale = ref<Locale>('en');

// Auto-detect browser locale or use stored locale if available
if (typeof window !== 'undefined') {
  const saved = localStorage.getItem('trevvelo_locale') as Locale;
  if (saved === 'en' || saved === 'fr' || saved === 'ar') {
    currentLocale.value = saved;
  }
}

// Watch locale changes to apply layout direction (RTL for Arabic, LTR for others)
watch(currentLocale, (newLocale) => {
  if (typeof window !== 'undefined') {
    localStorage.setItem('trevvelo_locale', newLocale);
    document.documentElement.dir = newLocale === 'ar' ? 'rtl' : 'ltr';
    document.documentElement.lang = newLocale;
  }
}, { immediate: true });

export function useI18n() {
  const setLocale = (locale: Locale) => {
    currentLocale.value = locale;
  };

  const t = (key: string): string => {
    const keys = key.split('.');
    let value: any = translations[currentLocale.value];

    for (const k of keys) {
      if (value && typeof value === 'object' && k in value) {
        value = value[k];
      } else {
        // Fallback to English if key is missing in active locale
        let fallback: any = translations['en'];
        for (const fk of keys) {
          if (fallback && typeof fallback === 'object' && fk in fallback) {
            fallback = fallback[fk];
          } else {
            fallback = key;
            break;
          }
        }
        return fallback;
      }
    }

    return typeof value === 'string' ? value : key;
  };

  return {
    locale: readonly(currentLocale),
    setLocale,
    t
  };
}

